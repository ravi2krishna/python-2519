# Inbuilt Modules 

# 1st syntax => import module (import whole module)
import math
print(math.sqrt(25))

# 2nd syntax => from module import functionality
from math import sqrt
print(sqrt(25))

# valid syntax 
import math as m
print(m.sqrt(25))

